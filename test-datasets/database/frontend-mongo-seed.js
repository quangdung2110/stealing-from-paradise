function upsertMany(collection, docs) {
  docs.forEach((doc) => {
    collection.updateOne(
      { _id: doc._id },
      { $set: doc },
      { upsert: true }
    );
  });
}

function safeCreateCollection(dbObj, name) {
  if (!dbObj.getCollectionNames().includes(name)) {
    dbObj.createCollection(name);
  }
}

